  public getNextLevelEntities(): Set<Entity> {
    //1 kollar på vår grid
    //2 och skapar alla entiter som behövs

    const entities = new Set<Entity>();

    for (let y in this.worldGrid) {
      for (let x in this.worldGrid) {
        const size = createVector(50,50);
        const position = createVector(50,50);

        switch (this.worldGrid[x][y]) {
          case 1 /* new x(position, fill, isSolid, damage,) (SPIKE)*/:
            entities.add(new Spike(position, "grey", true, true));
            break;

          case 2:
            entities.add(new Spike(position, "grey", false, false));
            break;

          case 3:
            entities.add(new Block(position, "green", true, false));
            break;

          case 4:
            entities.add(new Block(position, "none", true, true));
            break;

          case 5:
            entities.add(new Block(position, "none", true, true));
            break;

          case 6:
            entities.add(new Block(position, "green", true, true));
            break;

          case 7:
            entities.add(new Block(position, "green", false, false));
            break;

          case 8: //posistion, direction
            entities.add(new Enemy(position, "vertical"));
            break;

          case 9:
            entities.add(new Enemy(position, "horizontal"));
            break;

          case 10:
            entities.add(new FinishBlock(position, "green", false, true,));
            break;
        }
      }
    }
    return entities;
  }
